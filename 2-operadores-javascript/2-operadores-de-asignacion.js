// Los operadores de asignación son operadores que asignan un valor a una variable. Ya usamos el más básico, =,
// muchas veces. Simplemente asigna a la variable de la izquierda, el valor de la derecha:
//ejemplo:

let x = 3;
let y = 4;
x + y; //x ahora tiene el valor de y = 4
//Otros operadores de asignación que son útiles para
//reducir nuestro códigos y ser optimo

//ADICION ASIGNACION = +=
x += 4;
console.log(x); //7

//RESTA ASIGNACION = -=
x = 6;
x -= 4;
console.log(x); //2

//MULTIPLICACION ASIGNACION = *=
x = 2;
x *= 3;
console.log(x); //6

//DIVISION ASIGNACION = /=
x = 10;
x /= 5;
console.log(x); //2
