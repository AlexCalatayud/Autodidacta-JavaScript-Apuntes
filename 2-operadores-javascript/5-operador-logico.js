//Los operadores lógicos se utilizan normalmente con valores booleanos (lógicos);
//cuando lo son, devuelven un valor booleano.
//Sin embargo, los operadores && y || en realidad devuelven el valor de uno de los operandos especificados,
//por lo tanto pueden devolver un valor no booleano.

//operadores logicos:

//AND logico (&&): devuelve expr1 si se puede convetir a false ;
//de lo contrario, devuelve expr2. Por lo tanto se usa con valos booleanos
//&& devuelve trrue si ambos operando son true; de lo contrario devuelve false.

//OR logico (||) : devuelve expr1 si se puede convetir a true, de lo contrario
//devuelve expr2. Por lo tanto , cuando se una con su valores booleanos,
//  || devuelve true si alguno de los operandos es true , si ambos son falsos, devuelve false.

//NOT logico (!) : devuelve FALSE  si su unico operando se puede convertir a true, de lo contrario
// devuelve false.
