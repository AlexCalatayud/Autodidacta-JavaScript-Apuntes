// Vamos a poder realizar operaciones en JavaScript a través de los operadores. Básicamente son símbolos que ya
// conocemos (+, -, /, *)
//  que indican al intérprete de JavaScript las operaciones que debe realizar.

// Por ejemplo: Para el intérprete al ver el signo +, sabe que tiene que ejecutar la función suma
// (que tiene internamente definida), y toma como parámetros los términos que estén a la izquierda
// y la derecha del operador.

//SUMA :  +

const suma = 6 + 4;
console.log(suma); //10

//RESTA : -

const resta = 6 - 4;
console.log(resta); //2

//MULTIPLICACION: *

const multiplicacion = 2 * 5;
console.log(multiplicacion); //10

//DIVISION : /

const division = /*divide*/ 5 / 10;
console.log(division); //2

//SOBRANTE O MODULO : %

const modulo = 8 % 3;
console.log(modulo); //2
